package Modelo;

import java.sql.Date;
import java.sql.Time;


public class Horarios {
    int id;
    String nom;
    String ape;
    Date fecha;
    Time hora;
    
    public Horarios() {
    }
    
    public Horarios (String nom, String ape, Date fecha, Time hora ){
        this.nom = nom;
        this.ape = ape;
        this.fecha = fecha;
        this.hora = hora;
    
}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getApe() {
        return ape;
    }

    public void setApe(String ape) {
        this.ape = ape;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public Time getHora() {
        return hora;
    }

    public void setHora(Time hora) {
        this.hora = hora;
    }
       
    
    
}
